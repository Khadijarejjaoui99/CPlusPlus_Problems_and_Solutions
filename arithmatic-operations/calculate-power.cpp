#include <iostream>
using namespace std;

int main()
{
    int number;
    cout << "Please enter a number: \n";
    cin >> number;

    cout << "number^2 = " << number * number << "\n";
    cout << "number^3 = " << number * number * number << "\n";
    cout << "number^4 = " << number * number * number * number << "\n";
}